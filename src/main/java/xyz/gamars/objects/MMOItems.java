    public static MMOItem create(MMOItem mmoItem) {
        if (mmoItem.getId() == null || mmoItem.getId().isEmpty()) {
            System.out.println("Missing ID for item : " + mmoItem);
            return null;
        }

        ITEMS.put(mmoItem.getId(), mmoItem);
        return mmoItem;
    }

